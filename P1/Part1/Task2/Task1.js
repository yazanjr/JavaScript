m = prompt("Enter the month number")

switch(m){
    case "1" : 
        alert("Jan"); break;
    case "2" : 
        alert("Feb"); break;
    case "3" : 
        alert("Mar"); break;
    case "4" : 
        alert("Apr"); break;
    default: 
        alert("enter a number between 1 and 4"); break;
}
